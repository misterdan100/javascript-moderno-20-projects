let puntaje = 5;

// ++ para incrementar 1
puntaje++;

console.log(puntaje);

// otra forma de incrementar 1
++puntaje;

console.log(puntaje);

let puntaje2 = 5;

// como restar de a 1
puntaje2--;

console.log(puntaje2);

--puntaje2;

console.log(puntaje2);

// para sumar | asignar sumando
puntaje2 += 3;

console.log(puntaje2); // 6

